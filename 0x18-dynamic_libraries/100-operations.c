#include <stdio.h>

/**
 *add - function that does the operation
 *@a: first number
 *@b: end number
 *Return: the operation's result
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 *sub - function that does the operation
 *@a: first number
 *@b: end number
 *Return: the operation's result
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 *mul - function that does the operation
 *@a: first number
 *@b: end number
 *Return: the operation's result
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 *div - function that does the operation
 *@a: firstst number
 *@b: end number
 *Return: the operation's result
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 *mod - function that does the operation
 *@a: first number
 *@b: end number
 *Return: the result of opperation
 */

int mod(int a, int b)
{
	return (a % b);
}
